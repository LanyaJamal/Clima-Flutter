            children: <Widget>[
              Row(
                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                children: <Widget>[
                  CupertinoButton(
                    onPressed: () async {
                      var weatherData = await weather.getCityWeather(cityName);
                      UpdateUi(weatherData);
                    },
                    child: Icon(
                      Icons.near_me,
                      size: 50.0,
                    ),
                  ),
                  CupertinoButton(
                    onPressed: () async {
                      var typeName = await Navigator.push(
